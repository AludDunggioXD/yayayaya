// Fungsi untuk memuat lagu berdasarkan pilihan
function loadSong() {
  const selectedSong = document.getElementById("song").value;
  const lyricsContainer = document.getElementById("lyrics");

  if (selectedSong && songs[selectedSong]) {
    lyricsContainer.innerHTML = `<pre>${songs[selectedSong]}</pre>`;
  } else {
    lyricsContainer.innerHTML = `<p>Select a song to view its chords and lyrics.</p>`;
  }
}
// Tambahkan fitur autoscroll
let scrollInterval;

function startAutoScroll(speed) {
  clearInterval(scrollInterval); // Pastikan interval sebelumnya berhenti
  const lyricsDiv = document.getElementById("lyrics");
  scrollInterval = setInterval(() => {
    lyricsDiv.scrollBy(0, 1); // Scroll vertikal per piksel
    if (
      lyricsDiv.scrollTop + lyricsDiv.clientHeight >=
      lyricsDiv.scrollHeight
    ) {
      clearInterval(scrollInterval); // Hentikan saat mencapai akhir
    }
  }, speed);
}

function stopAutoScroll() {
  clearInterval(scrollInterval);
}

function initAutoScrollControls() {
  const controlContainer = document.createElement("div");
  controlContainer.id = "autoscroll-controls";
  controlContainer.innerHTML = `
    <label for="speed">Autoscroll Speed:</label>
    <select id="speed" onchange="updateScrollSpeed()">
      <option value="0">Off</option>
      <option value="95">1 (Slow)</option>
      <option value="80">2</option>
      <option value="70">3</option>
      <option value="60">4</option>
      <option value="40">5 (Fast)</option>
    </select>
  `;

  document.querySelector(".song-selector").appendChild(controlContainer);
}

function updateScrollSpeed() {
  const speed = document.getElementById("speed").value;
  if (speed == 0) {
    stopAutoScroll();
  } else {
    startAutoScroll(speed);
  }
}

// Inisialisasi kontrol autoscroll setelah DOM dimuat
document.addEventListener("DOMContentLoaded", () => {
  initAutoScrollControls();
});

